class TestOneShotLoopInvariants:
    """Tests derived from one_shot_loop.tla invariants."""

    def _make_dag(self):
        dag = RegistryDag()
        dag.add_node(Node.behavior(
            "gwt-test", "test_behavior",
            "a test input", "processed", "output produced",
        ))
        return dag

    # -- ValidState --

    def test_valid_state_initial(self):
        """ValidState: loop starts in idle state."""
        dag = self._make_dag()
        loop = OneShotLoop(dag=dag)
        assert loop.status.state == LoopState.IDLE

    def test_valid_state_after_query(self):
        """ValidState: after query, loop is in prompting_llm."""
        dag = self._make_dag()
        loop = OneShotLoop(dag=dag)
        loop.query("gwt-test")
        assert loop.status.state == LoopState.PROMPTING_LLM

    # -- TwoFailureLimit --

    def test_two_failure_limit_first_failure_retries(self):
        """TwoFailureLimit: first failure returns RETRY."""
        result, _ = route_result(TLCResult(success=False, error_message="fail"), 0)
        assert result == LoopResult.RETRY

    def test_two_failure_limit_second_failure_fails(self):
        """TwoFailureLimit: second consecutive failure returns FAIL."""
        result, _ = route_result(TLCResult(success=False, error_message="fail"), 1)
        assert result == LoopResult.FAIL

    def test_two_failure_limit_third_still_fails(self):
        """TwoFailureLimit: third consecutive failure also FAIL."""
        result, _ = route_result(TLCResult(success=False, error_message="fail"), 2)
        assert result == LoopResult.FAIL

    # -- DeterministicRouting --

    def test_deterministic_routing_pass(self):
        """DeterministicRouting: TLC pass always routes to PASS."""
        result, _ = route_result(TLCResult(success=True, states_found=100), 0)
        assert result == LoopResult.PASS

    def test_deterministic_routing_pass_after_failure(self):
        """DeterministicRouting: pass after prior failure still PASS."""
        result, _ = route_result(TLCResult(success=True, states_found=50), 1)
        assert result == LoopResult.PASS

    # -- DerivedConsistency --

    def test_derived_consistency_pass_resets_failures(self):
        """DerivedConsistency: done implies failures reset."""
        result, msg = route_result(TLCResult(success=True), 5)
        assert result == LoopResult.PASS

    # -- Extract / Format actions --

    def test_extract_pluscal_from_module(self):
        """ExtractFragment: extracts PlusCal from TLA+ module."""
        module = (
            "---- MODULE test ----\n"
            "EXTENDS Integers\n"
            "(* --algorithm Test\n"
            "variables x = 0;\n"
            "begin\n"
            "  L: x := 1;\n"
            "end algorithm; *)\n"
            "===="
        )
        result = extract_pluscal(module)
        assert result is not None
        assert "--algorithm" in result

    def test_extract_pluscal_returns_none(self):
        """ExtractFragment: returns None when no PlusCal found."""
        result = extract_pluscal("just some random text")
        assert result is None

    def test_format_prompt_after_query(self):
        """QueryContext -> PromptLLM: format_prompt returns context text."""
        dag = self._make_dag()
        loop = OneShotLoop(dag=dag)
        loop.query("gwt-test")
        prompt = loop.format_prompt()
        assert "gwt-test" in prompt
        assert len(prompt) > 0


