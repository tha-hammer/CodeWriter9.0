    # -- ClosureCorrect --

    def test_closure_correct_after_add_edge(self):
        """ClosureCorrect: closure matches actual transitive reachability."""
        dag = self._make_dag()
        dag.add_edge(Edge("a", "b", EdgeType.DEPENDS_ON))
        dag.add_edge(Edge("b", "c", EdgeType.DEPENDS_ON))
        # Transitive closure: a can reach b and c
        assert "b" in dag.closure.get("a", set())
        assert "c" in dag.closure.get("a", set())
        # b can reach c but not a
        assert "c" in dag.closure.get("b", set())
        assert "a" not in dag.closure.get("b", set())

    def test_closure_correct_single_edge(self):
        """ClosureCorrect: single edge creates direct reachability."""
        dag = self._make_dag()
        dag.add_edge(Edge("a", "b", EdgeType.DEPENDS_ON))
        assert "b" in dag.closure.get("a", set())
        assert "a" not in dag.closure.get("b", set())

    def test_closure_correct_empty(self):
        """ClosureCorrect: no edges means empty closure."""
        dag = self._make_dag()
        for node_id in dag.nodes:
            assert dag.closure.get(node_id, set()) == set() or len(dag.closure.get(node_id, set())) == 0

