SPEC_A = (
    "---- MODULE spec_a ----\n"
    "EXTENDS Integers\n"
    "VARIABLES x, shared_var\n"
    "\n"
    "(* definitions *)\n"
    "Init == x = 0 /\\ shared_var = 0\n"
    "ActionA == x\' = x + 1 /\\ UNCHANGED shared_var\n"
    "Next == ActionA\n"
    "InvA == x >= 0\n"
    "Spec == Init /\\ [][Next]_<<x, shared_var>>\n"
    "===="
)

SPEC_B = (
    "---- MODULE spec_b ----\n"
    "EXTENDS Integers\n"
    "VARIABLES y, shared_var\n"
    "\n"
    "(* definitions *)\n"
    "Init == y = 0 /\\ shared_var = 0\n"
    "ActionB == y\' = y + 1 /\\ UNCHANGED shared_var\n"
    "Next == ActionB\n"
    "InvB == y >= 0\n"
    "Spec == Init /\\ [][Next]_<<y, shared_var>>\n"
    "===="
)


class TestCompositionEngineInvariants:
    """Tests derived from composition_engine.tla invariants."""

    def test_valid_state_compose_produces_module(self):
        """ValidState: compose produces a valid ComposedModule."""
        mod_a = parse_tla(SPEC_A)
        mod_b = parse_tla(SPEC_B)
        composed = compose(mod_a, mod_b)
        assert isinstance(composed, ComposedModule)
        assert composed.name is not None

    def test_monotonic_growth_shared_vars_preserved(self):
        """MonotonicGrowth: shared variables preserved in composition."""
        mod_a = parse_tla(SPEC_A)
        mod_b = parse_tla(SPEC_B)
        composed = compose(mod_a, mod_b)
        assert "shared_var" in composed.shared_vars

    def test_no_empty_compose_both_have_vars(self):
        """NoEmptyCompose: both source modules must have variables."""
        mod_a = parse_tla(SPEC_A)
        mod_b = parse_tla(SPEC_B)
        composed = compose(mod_a, mod_b)
        assert len(composed.source_a.variables) > 0
        assert len(composed.source_b.variables) > 0

    def test_associativity_vars_subset_of_sources(self):
        """AssociativityHolds: composed vars are subset of source vars."""
        mod_a = parse_tla(SPEC_A)
        mod_b = parse_tla(SPEC_B)
        composed = compose(mod_a, mod_b)
        all_source = set(mod_a.variables) | set(mod_b.variables)
        all_composed = set(composed.shared_vars) | set(composed.a_only_vars) | set(composed.b_only_vars)
        assert all_composed.issubset(all_source)

    def test_derived_consistency_text_generated(self):
        """DerivedConsistency: composed module has generated TLA+ text."""
        mod_a = parse_tla(SPEC_A)
        mod_b = parse_tla(SPEC_B)
        composed = compose(mod_a, mod_b)
        assert len(composed.text) > 0
        assert "MODULE" in composed.text

    def test_valid_specs_parse_tla_extracts_variables(self):
        """ValidSpecs: parse_tla extracts variables correctly."""
        mod = parse_tla(SPEC_A)
        assert "x" in mod.variables
        assert "shared_var" in mod.variables


