"""cw9 CLI — entry point for using CodeWriter9 on external projects.

Usage:
    cw9 init [target_dir]     Initialize .cw9/ in the target directory
    cw9 status [target_dir]   Show project context and DAG summary
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

from registry.context import ProjectContext

# Engine root is always two levels up from this file:
# python/registry/cli.py -> python/registry -> python -> CodeWriter9.0
ENGINE_ROOT = Path(__file__).resolve().parent.parent.parent

_CONFIG_TEMPLATE = """\
# CodeWriter9 project configuration
# Generated by: cw9 init

[engine]
root = {engine_root!r}

[project]
# Add project-specific settings here
"""

_EMPTY_DAG = {
    "nodes": {},
    "edges": [],
    "test_artifacts": {},
}


def cmd_init(args: argparse.Namespace) -> int:
    target = Path(args.target_dir).resolve()

    if not target.is_dir():
        print(f"Error: {target} is not a directory", file=sys.stderr)
        return 1

    state_root = target / ".cw9"
    if state_root.exists() and not args.force:
        print(f".cw9/ already exists in {target}")
        print("Use --force to reinitialize.")
        return 1

    # Create directory structure
    dirs = [
        state_root / "schema",
        state_root / "specs",
        state_root / "bridge",
        state_root / "sessions",
    ]
    for d in dirs:
        d.mkdir(parents=True, exist_ok=True)

    # Write config
    config_path = state_root / "config.toml"
    config_path.write_text(
        _CONFIG_TEMPLATE.format(engine_root=str(ENGINE_ROOT))
    )

    # Write empty DAG
    dag_path = state_root / "dag.json"
    dag_path.write_text(json.dumps(_EMPTY_DAG, indent=2) + "\n")

    # Verify it loads
    ctx = ProjectContext.from_target(target, ENGINE_ROOT)

    print(f"Initialized .cw9/ in {target}")
    print()
    print(f"  engine_root:    {ctx.engine_root}")
    print(f"  target_root:    {ctx.target_root}")
    print(f"  state_root:     {ctx.state_root}")
    print(f"  schema_dir:     {ctx.schema_dir}")
    print(f"  spec_dir:       {ctx.spec_dir}")
    print(f"  artifact_dir:   {ctx.artifact_dir}")
    print(f"  session_dir:    {ctx.session_dir}")
    print(f"  test_output_dir:{ctx.test_output_dir}")
    print()
    print("Next steps:")
    print("  1. Add JSON schemas to .cw9/schema/")
    print("  2. Run: cw9 status")

    return 0


def cmd_status(args: argparse.Namespace) -> int:
    target = Path(args.target_dir).resolve()
    state_root = target / ".cw9"

    if not state_root.exists():
        print(f"No .cw9/ found in {target}", file=sys.stderr)
        print("Run: cw9 init", file=sys.stderr)
        return 1

    ctx = ProjectContext.from_target(target, ENGINE_ROOT)

    print(f"CodeWriter9 project: {ctx.target_root}")
    print(f"  engine: {ctx.engine_root}")
    print()

    # DAG summary
    dag_path = ctx.schema_dir.parent / "dag.json"
    if dag_path.exists():
        dag_data = json.loads(dag_path.read_text())
        n_nodes = len(dag_data.get("nodes", {}))
        n_edges = len(dag_data.get("edges", []))
        print(f"  DAG: {n_nodes} nodes, {n_edges} edges")
    else:
        print("  DAG: not found")

    # Schema count
    schemas = list(ctx.schema_dir.glob("*.json"))
    print(f"  Schemas: {len(schemas)}")

    # Spec count
    specs = list(ctx.spec_dir.glob("*.tla"))
    print(f"  Specs: {len(specs)}")

    return 0


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="cw9",
        description="CodeWriter9 — formally verified code generation",
    )
    sub = parser.add_subparsers(dest="command")

    # init
    p_init = sub.add_parser("init", help="Initialize .cw9/ in a target directory")
    p_init.add_argument("target_dir", nargs="?", default=".", help="Target project directory (default: .)")
    p_init.add_argument("--force", action="store_true", help="Reinitialize existing .cw9/")

    # status
    p_status = sub.add_parser("status", help="Show project context and DAG summary")
    p_status.add_argument("target_dir", nargs="?", default=".", help="Target project directory (default: .)")

    args = parser.parse_args(argv)

    if args.command == "init":
        return cmd_init(args)
    elif args.command == "status":
        return cmd_status(args)
    else:
        parser.print_help()
        return 0


if __name__ == "__main__":
    sys.exit(main())
