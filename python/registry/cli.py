def _extract_json_object(text: str) -> dict:
    """Extract the first valid JSON object from text that may contain surrounding prose."""
    start = text.find("{")
    if start == -1:
        raise ValueError(f"No JSON object found in response: {text[:200]}")

    # Use a brace-depth counter to find matching close braces.
    # Re-anchor `start` each time depth transitions 0→1 so that after a
    # failed balanced block we try the NEXT top-level object, not the slice
    # from the original `{`.
    depth = 0
    in_string = False
    escape = False
    for i in range(start, len(text)):
        ch = text[i]
        if escape:
            escape = False
            continue
        if ch == "\\":
            if in_string:
                escape = True
            continue
        if ch == '"' and not escape:
            in_string = not in_string
            continue
        if in_string:
            continue
        if ch == "{":
            if depth == 0:
                start = i  # anchor to this top-level brace
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0:
                try:
                    return json.loads(text[start:i + 1])
                except json.JSONDecodeError:
                    # This balanced block wasn't valid JSON; keep scanning
                    # depth is already 0, so the next '{' will re-anchor start
                    continue

    raise ValueError(f"Could not parse JSON from response: {text[:200]}")


def _build_extraction_prompt(skeleton, body: str, error_feedback: str | None = None) -> str:
    """Build the LLM prompt for IN:DO:OUT extraction from a function skeleton."""
    parts = [
        f"## Function: {skeleton.function_name}",
        f"File: {skeleton.file_path}:{skeleton.line_number}",
    ]
    if skeleton.class_name:
        parts.append(f"Class: {skeleton.class_name}")
    if skeleton.params:
        param_strs = [f"{p.name}: {p.type or 'Any'}" for p in skeleton.params]
        parts.append(f"Params: {', '.join(param_strs)}")
    if skeleton.return_type:
        parts.append(f"Returns: {skeleton.return_type}")
    parts.append(f"\n## Body\n```\n{body}\n```")
    if error_feedback:
        parts.append(f"\n## Previous Error\n{error_feedback}\nPlease fix the JSON output.")
    return "\n".join(parts)


def _data_to_fn_record(data: dict, skeleton) -> "FnRecord":
    """Convert parsed JSON extraction data to a FnRecord."""
    from registry.crawl_types import (
        FnRecord, InField, InSource, OutField, OutKind,
    )

    source_map = {
        "parameter": InSource.PARAMETER,
        "state": InSource.STATE,
        "literal": InSource.LITERAL,
        "internal_call": InSource.INTERNAL_CALL,
        "external": InSource.EXTERNAL,
    }
    kind_map = {
        "ok": OutKind.OK,
        "err": OutKind.ERR,
        "side_effect": OutKind.SIDE_EFFECT,
        "mutation": OutKind.MUTATION,
    }

    ins = [
        InField(
            name=i["name"],
            type_str=i.get("type_str", "Any"),
            source=source_map.get(i.get("source", "parameter"), InSource.PARAMETER),
            source_function=i.get("source_function"),
            source_file=i.get("source_file"),
            description=i.get("description", ""),
        )
        for i in data.get("ins", [])
    ]
    outs = [
        OutField(
            name=kind_map.get(o.get("name", "ok"), OutKind.OK),
            type_str=o.get("type_str", "Any"),
            description=o.get("description", ""),
        )
        for o in data.get("outs", [])
    ]

    return FnRecord(
        uuid=PLACEHOLDER_UUID,
        function_name=skeleton.function_name,
        class_name=skeleton.class_name,
        file_path=skeleton.file_path,
        line_number=skeleton.line_number,
        src_hash=skeleton.file_hash,
        ins=ins,
        do_description=data.get("do_description", ""),
        do_steps=data.get("do_steps", []),
        outs=outs,
        failure_modes=data.get("failure_modes", []),
        operational_claim=data.get("operational_claim", ""),
        skeleton=skeleton,
    )


def _build_async_extract_fn(model: str = DEFAULT_MODEL):
    """Build an async extract_fn using standalone query() — fresh context per call.

    Each invocation of the returned coroutine creates a completely independent
    session via the SDK's standalone query() function. No persistent client is
    constructed — context isolation is guaranteed by design.
    """
    import os
    import sys
    os.environ.pop("CLAUDECODE", None)
    from claude_agent_sdk import query as _query, ClaudeAgentOptions, AssistantMessage, ResultMessage, TextBlock
    from registry.crawl_types import FnRecord, Skeleton
    # Store query at module level so it can be patched in tests
    sys.modules[__name__].query = _query

    async def extract_fn(skeleton: Skeleton, body: str, error_feedback: str | None = None) -> FnRecord:
        prompt = _build_extraction_prompt(skeleton, body, error_feedback)

        options = ClaudeAgentOptions(
            allowed_tools=[],
            system_prompt=_EXTRACT_SYSTEM_PROMPT,
            max_turns=4,  # model may waste turns on prose; SDK needs extra for structured output
            model=model,
            output_format={"type": "json_schema", "schema": _EXTRACT_OUTPUT_SCHEMA},
        )

        data = None
        result_subtype = None
        result_text = []
        # Use module-level query (supports test patching)
        _cli_mod = sys.modules[__name__]
        async for message in _cli_mod.query(prompt=prompt, options=options):
            if isinstance(message, ResultMessage):
                result_subtype = getattr(message, "subtype", None)
                if message.structured_output:
                    data = message.structured_output
                elif message.result:
                    result_text.append(message.result)
            elif isinstance(message, AssistantMessage):
                for block in message.content:
                    if isinstance(block, TextBlock):
                        result_text.append(block.text)

        if data is not None:
            pass  # structured output succeeded
        else:
            import sys as _sys
            print(
                f"  [debug] {skeleton.function_name}: structured_output=None "
                f"subtype={result_subtype} text_len={sum(len(t) for t in result_text)} "
                f"preview={(''.join(result_text))[:80]!r}",
                file=_sys.stderr,
            )
        if data is not None:
            pass
        elif result_subtype in ("error_max_turns", "error_max_structured_output_retries"):
            raw_preview = "".join(result_text)[:200]
            raise ValueError(
                f"Structured output failed ({result_subtype}): {raw_preview}"
            )
        else:
            # No structured_output but no error either (success without it,
            # or no ResultMessage at all e.g. in tests) — fall back to text parsing
            raw = "".join(result_text)
            data = _extract_json_object(raw)

        return _data_to_fn_record(data, skeleton)

    return extract_fn


def cmd_cleanup(args: argparse.Namespace) -> int:
    """Remove stale cw9 temp directories from /tmp."""
    import time

    max_age_hours = getattr(args, "max_age", 24)
    dry_run = getattr(args, "dry_run", False)
    tmp = Path("/tmp")
    prefixes = ("cw9_",)
    cutoff = time.time() - (max_age_hours * 3600)

    removed = 0
    freed_bytes = 0
    errors = 0

    for entry in sorted(tmp.iterdir()):
        if not entry.is_dir():
            continue
        if not any(entry.name.startswith(p) for p in prefixes):
            continue
        try:
            mtime = entry.stat().st_mtime
        except OSError:
            continue
        if mtime > cutoff:
            continue

        # Calculate size before removal
        dir_size = 0
        try:
            for f in entry.rglob("*"):
                try:
                    dir_size += f.stat().st_size
                except OSError:
                    pass
        except OSError:
            pass

        if dry_run:
            age_h = (time.time() - mtime) / 3600
            print(f"  [dry-run] {entry.name}  {_fmt_size(dir_size)}  {age_h:.0f}h old")
            removed += 1
            freed_bytes += dir_size
            continue

        try:
            shutil.rmtree(entry)
            removed += 1
            freed_bytes += dir_size
        except OSError as e:
            print(f"  ! Failed to remove {entry.name}: {e}", file=sys.stderr)
            errors += 1

    # Also prune git worktrees if any were in /tmp
    if not dry_run:
        import subprocess
        try:
            subprocess.run(
                ["git", "worktree", "prune"],
                cwd=str(Path.cwd()),
                capture_output=True,
                timeout=10,
            )
        except Exception:
            pass

    action = "Would remove" if dry_run else "Removed"
    print(f"\n{action} {removed} director{'y' if removed == 1 else 'ies'}, freeing {_fmt_size(freed_bytes)}")
    if errors:
        print(f"  ({errors} failed)")
    return 0


def _fmt_size(nbytes: int) -> str:
    """Format byte count as human-readable size."""
    for unit in ("B", "KB", "MB", "GB"):
        if nbytes < 1024:
            return f"{nbytes:.1f}{unit}"
        nbytes /= 1024
    return f"{nbytes:.1f}TB"


def cmd_stale(args: argparse.Namespace) -> int:
    """Check which ingested nodes are stale (file hashes changed)."""
    import hashlib

    target = Path(args.target_dir).resolve()
    state_root = _require_cw9(target)
    if state_root is None:
        return 1

    crawl_db_path = state_root / "crawl.db"
    if not crawl_db_path.exists():
        print("No crawl.db found. Run: cw9 ingest", file=sys.stderr)
        return 1

    from registry.crawl_store import CrawlStore

    with CrawlStore(crawl_db_path) as store:
        # Compute current file hashes
        records = store.get_all_records()
        file_paths = {r.file_path for r in records}
        current_hashes: dict[str, str] = {}
        for fp in file_paths:
            p = Path(fp)
            if p.exists():
                current_hashes[fp] = hashlib.sha256(p.read_bytes()).hexdigest()

        direct_stale = store.get_stale_records(current_hashes)
        all_stale = store.get_transitive_stale(direct_stale) if direct_stale else []

    if not all_stale:
        print("No stale records.")
        return 0

    print(f"{len(all_stale)} stale record(s) ({len(direct_stale)} directly changed):")
    for uid in all_stale:
        rec = next((r for r in records if r.uuid == uid), None)
        if rec:
            marker = " [direct]" if uid in direct_stale else " [transitive]"
            print(f"  {rec.function_name} @ {rec.file_path}{marker}")
    return 0


def cmd_seams(args: argparse.Namespace) -> int:
    """Check seam compatibility across function boundaries in crawl.db."""
    target = Path(args.target_dir).resolve()
    state_root = _require_cw9(target)
    if state_root is None:
        return 1

    crawl_db_path = state_root / "crawl.db"
    if not crawl_db_path.exists():
        print("No crawl.db found. Run: cw9 ingest", file=sys.stderr)
        return 1

    from registry.crawl_store import CrawlStore
    from registry.seam_checker import check_all_seams, render_seam_report, seam_report_to_json

    # Load cross-cutting rules if requested
    behavioral_report = None
    if getattr(args, "cross_cutting", False):
        from registry.seam_checker import (
            check_behavioral_contracts,
            load_cross_cutting_rules,
        )
        rules_path = state_root / "schema" / "cross_cutting_rules.json"
        if not rules_path.exists():
            print(f"Cross-cutting rules file not found: {rules_path}", file=sys.stderr)
            return 1
        try:
            rules = load_cross_cutting_rules(rules_path)
        except (ValueError, FileNotFoundError) as exc:
            print(f"Error loading cross-cutting rules: {exc}", file=sys.stderr)
            return 1

    with CrawlStore(crawl_db_path) as store:
        report = check_all_seams(store)
        if getattr(args, "cross_cutting", False):
            behavioral_report = check_behavioral_contracts(store, rules)

    if getattr(args, "output_json", False):
        import json
        data = seam_report_to_json(report)
        if behavioral_report is not None:
            from registry.seam_checker import behavioral_report_to_json
            data["behavioral"] = behavioral_report_to_json(behavioral_report)
        print(json.dumps(data, indent=2))
    else:
        print(render_seam_report(report, verbose=getattr(args, "verbose", False)))
        if behavioral_report is not None:
            from registry.seam_checker import render_behavioral_report
            print(render_behavioral_report(behavioral_report))

    return 1 if report.mismatches else 0


def cmd_show(args: argparse.Namespace) -> int:
    """Show information about a node. With --card, show IN:DO:OUT card."""
    target = Path(args.target_dir).resolve()
    state_root = _require_cw9(target)
    if state_root is None:
        return 1
    node_id = args.node_id

    if getattr(args, "card", False):
        crawl_db_path = state_root / "crawl.db"
        if not crawl_db_path.exists():
            print("No crawl.db found. Run: cw9 ingest", file=sys.stderr)
            return 1
        from registry.crawl_store import CrawlStore
        with CrawlStore(crawl_db_path) as store:
            text = store.get_card_text(node_id)
            print(text)
        return 0

    # Default: show DAG node info
    dag_path = state_root / "dag.json"
    if not dag_path.exists():
        print("No dag.json found. Run: cw9 extract or cw9 ingest", file=sys.stderr)
        return 1

    from registry.dag import RegistryDag
    dag = RegistryDag.load(dag_path)
    if node_id not in dag.nodes:
        print(f"Node not found: {node_id}", file=sys.stderr)
        return 1

    node = dag.nodes[node_id]
    print(f"ID: {node.id}")
    print(f"Kind: {node.kind.value}")
    print(f"Name: {node.name}")
    if node.description:
        print(f"Description: {node.description}")
    if node.given:
        print(f"Given: {node.given}")
    if node.when:
        print(f"When: {node.when}")
    if node.then:
        print(f"Then: {node.then}")

    # Show edges
    edges = [e for e in dag.edges if e.from_id == node_id or e.to_id == node_id]
    if edges:
        print(f"\nEdges ({len(edges)}):")
        for e in edges:
            print(f"  {e.from_id} --{e.edge_type.value}--> {e.to_id}")

    return 0


